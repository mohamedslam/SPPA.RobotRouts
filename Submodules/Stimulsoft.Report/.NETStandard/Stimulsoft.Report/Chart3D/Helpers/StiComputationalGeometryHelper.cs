using System.Collections.Generic;
using System.Drawing;
using System.Linq;

namespace Stimulsoft.Report.Chart
{
    internal class StiComputationalGeometryHelper
    {
        #region Methods
        public static bool IsPointInPolygon(PointF[] polygon, PointF testPoint)
        {
            bool result = false;
            int j = polygon.Count() - 1;
            for (int i = 0; i < polygon.Count(); i++)
            {
                if (polygon[i].Y < testPoint.Y && polygon[j].Y >= testPoint.Y || polygon[j].Y < testPoint.Y && polygon[i].Y >= testPoint.Y)
                {
                    if (polygon[i].X + (testPoint.Y - polygon[i].Y) / (polygon[j].Y - polygon[i].Y) * (polygon[j].X - polygon[i].X) < testPoint.X)
                    {
                        result = !result;
                    }
                }
                j = i;
            }
            return result;
        }

        public static PointF[] ConvexHull(List<PointF> points)
        {
            var list = new LinkedList<PointF>(points.AsEnumerable());
            var wrappingPoints = StiJarvisMarchAlgorithm.Calculate(list);

            return wrappingPoints.ToArray();
        } 
        #endregion
    }
}
