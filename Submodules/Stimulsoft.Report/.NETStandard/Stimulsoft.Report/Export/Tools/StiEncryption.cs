using System;

namespace Stimulsoft.Report.Export.Tools
{
    public class StiEncryption
    {
        public static void RC4(ref byte[] bytes, byte[] key)
        {
            var s = new byte[256];
            var k = new byte[256];
            byte temp;
            int i, j;

            var keyLength = key.GetLength(0);
            for (i = 0; i < 256; i++)
            {
                s[i] = (byte)i;
                k[i] = key[i % keyLength];
            }

            j = 0;
            for (i = 0; i < 256; i++)
            {
                j = (j + s[i] + k[i]) % 256;
                temp = s[i];
                s[i] = s[j];
                s[j] = temp;
            }

            i = j = 0;
            var bytesLength = bytes.GetLength(0);
            for (var x = 0; x < bytesLength; x++)
            {
                i = (i + 1) % 256;
                j = (j + s[i]) % 256;
                temp = s[i];
                s[i] = s[j];
                s[j] = temp;
                var t = (s[i] + s[j]) % 256;
                bytes[x] ^= s[t];
            }
        }
    }
}
